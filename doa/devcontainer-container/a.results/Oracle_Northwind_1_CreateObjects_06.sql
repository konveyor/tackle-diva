CREATE TABLE CUSTOMERS 
( 
  CUSTOMERID  CHAR(5) NOT NULL, 
  COMPANYNAME  VARCHAR(40) NOT NULL, 
  CONTACTNAME  VARCHAR(30), 
  CONTACTTITLE  VARCHAR(30), 
  ADDRESS  VARCHAR(60), 
  CITY  VARCHAR(15), 
  REGION  VARCHAR(15), 
  POSTALCODE  VARCHAR(10), 
  COUNTRY  VARCHAR(15), 
  PHONE  VARCHAR(24), 
  FAX  VARCHAR(24), 
CONSTRAINT PK_CUSTOMERS 
  PRIMARY KEY (CUSTOMERID)
) 
;